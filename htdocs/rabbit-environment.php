      <h3>If your rabbits live outside</h3>
<ul>
  <li>
    <p>Your rabbits will need a roomy hutch and an exercise run (or an enclosure containing both) that is high enough to allow the rabbits to stand-up fully on their hind legs, as well as being big enough to allow plenty of room for them to hop around. Place the hutch in a position that faces the morning sun. Make sure that your rabbits have access to shade, especially on sunny days, and that they are protected from the rain, wind, and any other adverse weather conditions.</p>
  </li>
  <h3>Hutch and run size</h3>
  <li>
    <p>Many hutches sold in shops are far too small for rabbits to live in. Your hutch should be no smaller than 2m long. We recommend a minimum hutch size of 3m (length) x 1.5m (width) x 0.75m (height) or the equivalent area with other dimensions. However, the bigger the better and you should get/make a hutch as big as you can afford/manage/fit! The hutch should connect to, or be contained within, a run (which should be as large as possible) to allow your rabbits to hop, run, jump, and stand fully upright on their back legs.</p>
  </li>
  <h3>Hutch requirements</h3>
  <li>
    <p>The sleeping area should be raised off the ground slightly and must be weatherproof and draught-proof. Your hutch must be predator proof, as dogs, cats, and other predators can kill your rabbits. There should be areas for the rabbits to hide and play. There should be raised areas for your rabbits to sit on, sunbath</p>
  </li>
</ul>


<h3>If your rabbits live inside</h3>
<ul>
  <li>
    <p>The rabbits need a safe (rabbit-proof, i.e. that they cannot escape from!) place for sleeping and for unsupervised play, which includes a water bottle/bowl, hay, bedding, food dish, litter tray, and toys. Close supervision when loose in the house in non-rabbit proof areas. Ideally, access to an outdoor area for additional exercise and enrichment.</p>
  </li>
  <li>
    <h3>A rabbit-proof environment</h3>
  </li>
  <li>
    <p>Tape electrical and phone cords where they cannot be chewed (don’t forget those things that are up quite high but which your rabbits could reach when they are on their hind legs). Consider getting cord protectors and covers for electrical outlets. Move potted plants where they cannot be eaten, as they are likely to be poisonous to rabbits. Remove children’s toys, clothing, etc. that could be dangerous and other things that you don’t want to be chewed.</p>
  </li>
  <li>
    <h3>Exercise</h3>
  </li>
  <li>
    <p>You can train your rabbits to use a cat flap if you would like to give them easy access to a secure and safe outside area. If your rabbits do not have free access to an outside area, you should consider providing them with daily exercise in the garden where they are free to hop around for at least one hour a day. This area would need to be secure and rabbit-proof and might consist of a large exercise pen.</p>
  </li>
</ul>

