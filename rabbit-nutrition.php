      <p>Rabbits are herbivores, meaning they eat mostly plants and plant-based foods. A balanced diet, high in fiber, is essential for rabbits – feeding them the wrong thing can lead to serious problems such as dental disease and gut stasis. If you need to make changes to your rabbits’ diet, it’s important to do so slowly to prevent an upset stomach. The best diet for your bunnies is one that’s as close to a wild rabbit’s diet as possible. Our vets recommend the following:</p>
<p>Hay & grass – ideally an unlimited amount, but at least a bundle that’s as big as them each day.</p>
<p>Fresh food – an adult-sized handful of leafy greens, vegetables and herbs twice daily.</p>
<p>Nuggets – just one tablespoon of rabbit nuggets once daily (or twice daily if your rabbits weigh over 3.5kg).</p>
<p>Water – constant access to clean, fresh drinking water.</p>
<p>Hay and grass</p>
<p>Around 85% of your rabbits’ diet should be hay and/or grass – ideally, they should be given an unlimited amount, but as an absolute minimum, a bundle at least as big as them per day. It’s important to know the difference between ‘feeding hay’ and ‘bedding hay’ – they are both dried grasses, but nutritionally, they are very different. ‘Feeding hay’ is fresher, greener, smells better, tastes better, and has more nutrients in it. ‘Bedding hay’ is dryer, ideal for bedding, but not so great for nibbling, although it's not a problem if your rabbits occasionally eat it!</p>
<p>Greens and fresh veg</p>
<p>Ideally your rabbits should have five to six different types of fresh plant/grass/veg every day to give them a good balance of vitamins and minerals. We’ve listed some of the safe foods for rabbits below:</p>
<h3>Safe vegetables</h3>
<p>Bell peppers (red, yellow and green)</p>
<p>Broccoli – in moderation (can cause gas)</p>
<p>Brussel Sprouts – in moderation (can cause gas)</p>
<p>Cabbage (dark green varieties) – in moderation</p>
<p>Carrot tops</p>
<p>Cauliflower including the leaves and stalks</p>
<p>Celery including the leaves</p>
<p>Chicory</p>
<p>Courgette (and the flowers but not the leaves)</p>
<p>Fennel</p>
<p>Jerusalem artichoke and leaves</p>
<p>Kale</p>
<p>Mange tout</p>
<p>Salad leaves (such as romaine lettuce) – in moderation</p>
<p>Rocket</p>
<p>Spring greens</p>
<p>Spinach – in moderation</p>
<p>Watercress</p>
<p>Safe Herbs</p>
<p>Basil</p>
<p>Coriander</p>
<p>Dill</p>
<p>Mint</p>
<p>Parsley</p>
<p>Thyme</p>
<h3>Rabbit food (nuggets/pellets)</h3>
<p>As well as hay, grass and fresh food, you should give each of your rabbits one tablespoon of nuggets every day (or twice a day if they’re over 3.5kg). Although this seems like a small amount, dried rabbit nuggets are very concentrated, which is why your bunnies don’t need a lot. If your rabbits are fed too many pellets, they will eat less hay, which can lead to health problems such as overgrown teeth and gut stasis. Even nuggets containing long fiber or high levels of fiber should be fed in small quantities. It’s a good idea to scatter the pellets around your rabbits’ home to encourage them to forage.</p>
